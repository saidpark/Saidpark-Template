Historique de ce logiciel
La toute première édition de 3D Studio sort en 1990. Développé par le Groupe Yost sous environnement Atari ST, elle tourne uniquement sous MS-DOS. Elle gère des formes simples sur lesquelles il est possible d'appliquer 3 types d'ombres et un effet miroir en mode VGA. Les versions suivantes verront les effets et formes s'ajouter ainsi que l'application automatique de certaines fonctions. Autodesk ajoute ensuite un langage de script simple et la possibilité d'ajouter des plugins externes. En 1996, 3D Studio devient Kinetix 3D Studio Max. Il voit son mode de fonctionnement et surtout son moteur de rendu changer en profondeur (orientation objet et gestion de formes complexes). Durant les années 2000, 3Ds Max prend son envole pour devenir l'un des logiciels incontournables dans le domaine des jeux vidéo et du cinéma, notamment grâce à la puissance de son langage de Script MaxScript.
 
Description du Logiciel
 
3D Studio Max est un logiciel de 3D. Il permet donc de modéliser et d'animer des modules et formes plus ou moins complexes. S'il reste réservé à un public très spécialisé durant la grande majorité de son existence, il s'ouvre progressivement à un public de passionnés et de créatifs amateurs. 
 
Il permet par exemple de réaliser des titrages complexes, d'animer un texte en 3D et plus encore de créer un personnage puis de lui donner vie. Il est également possible d'appliquer différentes textures sur les éléments travaillés pour leur donner l'apparence souhaitée (peau et système pileux pour un personnage ou texture métallique pour une fourchette par exemple).
 
Les utilisateurs les plus avancés pourront pousser l'expérience encore plus loin en réalisant des scènes complètes incluant un décor et un environnement (ville durant une guerre par exemple) dans lequel des personnages interagissent.
 
Grâce à différents plugins, il est également possible de travailler un projet en parallèle entre 3D Studio Max et d'autres logiciels comme After Effect. Ces passerelles ouvrent des perspectives de création très vastes.
 
 
