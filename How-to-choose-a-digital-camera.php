      <p class="Style6">Note that cameras with zoom often have a range of maximum apertures listed, such as f/2.5 - f/4.0. This means the maximum aperture at the most wide angle setting is f/2.5, and the maximum aperture at the most telephoto setting is f/4.0</p>
      <h2>Lens zoom range</h2>
      <blockquote>
        <p class="Style6">A zoom lens has a variable focal length. The focal length determines the magnification of the lens. A short focal length is a wide-angle lens, great for taking in large vistas. A long focal length is a telephoto lens, allowing you to get a tight photo of a distant object or person. A zoom lens lets you combine both of these and everything in between into a single adjustable lens. If a camera has a 3X zoom, it means that the longest focal length is 3 times the shortest.</p>
      </blockquote>
      <table width="580" border="0">
        <tr>
          <td width="359"><p class="Style6">Most digital cameras have a 3X zoom, with a focal length range from around 35 mm to 105 mm. 35 mm is a modest wide angle, and 105 mm is a modest telephoto. Superzoom cameras have extreme zoom ranges of 8X or 10X. These generally have about the same minimum focal length of around 35mm, but these usually have a much longer maximum focal length. Digital SLR's almost always have interchangeable lenses that allow you to pick whatever focal length you want.</p>          </td>
          <td width="211"><div align="right"><img src="celebrities/camera-lens-icon.jpg" alt="digital camera lens" width="210" height="160"></div></td>
        </tr>
      </table>
      <span class="Style6">Long focal lengths mean you can get a tight photo of your children's faces, or a shot on the soccer field where you child is actually recognizable. Extreme focal lengths let you get in real close to the action even from the sidelines of a soccer field.</span>
      <p class="Style6"><strong class="Style14">(Important: ignore "digital zoom" specifications in ads. Only pay attention to optical zoom.</strong> Digital zoom is of no value. All it does is crop the image in the camera. You can always crop an image in software after you have transferred it to your computer, and you have more freedom at this time.)<br>
      Long focal lengths present problems for photographers. It is hard to hold a camera sufficiently stable at a focal length of 300 mm. Bright sunlight helps, and a tripod or monopod can do wonders. An increasing number of cameras with extreme zooms have <em>stabilized</em> lenses that compensate for camera shake. Good stabilization can make a remarkable difference in sharpness of handheld shots at extreme focal lengths. Do not buy a superzoom without image stabilization unless you plan on using a tripod.<br>At the time of this writing, I believe most people are best served by one of three classes of cameras:</p>
      <ul>
        <li class="Style6">Ultracompacts with a zoom of roughly 3X. The great virtue of these is that you can always have your camera with you. The quality of the better ultracompacts is astounding for their size.</li>
        <li class="Style6">Superzooms with a zoom ratio of roughly 10X - 12X. Only a little more than the better ultracompacts, they let you reach out for the distant photos. Be sure to get image stabilization.</li>
        <li class="Style6">Digital SLRs. This is what the pros use, and for good reason. You can choose any of a wide variety of lenses (for a price), and you can take beautiful photos with less light. But these are big and expensive.</li>
      </ul>
      <h2>Lens quality</h2>
      <blockquote>
        <p class="Style6">Lenses with the same focal length and aperture can differ substantially in quality. A poor lens is not as sharp, and it may exhibit chromatic aberration, which means that all colors are not brought to the same focus. This usually shows up as colored fringes at high contrast edges.</p>
        <p class="Style6">Unlike aperture and zoom range, you cannot read the lens quality of the camera box. The two best approaches to getting a good lens are (1) read reviews, and (2) stick with reputable <em>camera</em>companies. I tend to trust camera companies such as Olympus, Nikon, and Canon more than electronics companies who only moved into cameras with the advent of digital photography. I think it is easier of a camera manufacturer to incorporate good electronics than for a camera company to incorporate good optics. A few of the electronics companies are buying optics from reputable lens companies.</p>
      </blockquote>
      <h2>In-camera Software</h2>
      <blockquote>
        <p class="Style6">Here I am not talking about photo software that comes with your computer, but rather the software built into the camera. Taking a digital photo means a fair amount of computation. When you snap a digital photo, the camera first grabs a "pre-photo" to determine the brightness and color balance of the scene. It then shoots the real picture, based on the earlier information.</p>
        <p class="Style6">This is an important calculation. The importance of brightness is obvious. Less obvious is how radically the colors of different light sources are. Slide photographers no this. If you shoot standard "daylight" film indoors under incandescent lights, everything is orange. Film photographers see less of a problem, because the processing labs that print our photos correct for most of these color shifts. With digital photography, you want the camera to do all of this color compensation for you. This is not easy, since the camera needs to distinguish between incandescent illumination and a daylight scene with a lot of red and orange colors.</p>
        <p class="Style6">The good news is that some cameras do a remarkable job, giving you better color rendition than you have ever seen short of expensive custom work by custom photo labs. My own sense is that Olympus does the best, with Canon and Nikon very close seconds. Sony seems to lag behind in this regard</p>
      </blockquote>
      <h2>Media type</h2>
      <blockquote>
        <p class="Style6">Most digital cameras use CompactFlash or Secure Digital. The difference is not worth worrying about unless you already have a good supply of one type. Many Sony cameras use their own proprietary "memory sticks". I prefer to avoid proprietary solutions, as it restricts your future choices considerably. The trend is toward Secure Digital.</p>
      </blockquote>
