          <H1>How to choose a digital camera </H1>
          <BLOCKQUOTE>
            <h2 align="left" class="Style6">The quality of a digital camera is mainly defined by four quantities:   resolution, lens aperture, lens zoom range, lens quality, and software. CCD   sensitivity also varies somewhat.<br>
              <img src="celebrities/P712Back.jpg" alt="digital camera" width="300" height="250" align="middle"><br>
  <span class="Style12">Resolution</span></h2>
          </BLOCKQUOTE>
          <BLOCKQUOTE>
            <p align="left" class="Style6">Nearly all digital cameras use CCD's as the sensing element. This is what   takes the place of film. The resolution is the number of pixels in the captured   image. Computer images are divided into little dots called pixels. The more   pixels, the more detailed the image can be. Here is a guide to choosing   resolution, estimating the size print you can make from each:</p>
            <UL>
              <LI><span class="Style6">1.3 Megapixel = 1280x960: Great 4x6's, acceptable 5x7's.   
              </span>
              <LI><span class="Style6">2 Megapixel = 1600x1200: Pretty good 8x10's.   
              </span>
              <LI><span class="Style6">3 Megapixel = 2048x1536: Great 8x10's, good 11x14's.   
              </span>
              <LI><span class="Style6">4 Megapixel = 2272 x 1704: Great 11x14's, and acceptable 16x20's.   
              </span>
              <LI><span class="Style6">5 Megapixel = 2560x1920: Pretty good 16x20's.   
              </span>
              <LI class="Style6">6 - 10 Megapixels = At this point you are usually limited by the lens, not   the pixels. </LI>
            </UL>
            <p class="Style6">Casual photographers are satisfied with bigger prints from each size, while   those who like to look at 8x10's from a distance of three inches think I am   being too generous in the above evaluations.</p>
          </BLOCKQUOTE>
          <h2>Lens aperture</h2>
          <blockquote>
            <p class="Style6">The aperture of a lens is its maximum opening. The bigger the aperture, the more light is gathered, and the less light you need to take a good photo. This is the most overlooked lens specification, but it very important, especially if you like to take photos indoors without flash or from a reasonable distance.<br>Lens aperture is measured in f/numbers, such as f/2.0 or f/3.5. An aperture of f/2.0 literally means that the lens opening is half the focal length of the lens. Thus, smaller numbers mean bigger lens openings. You would rather have a lens that is f/2.0 than a lens that is f/4.0          </p>
